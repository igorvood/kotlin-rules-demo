package ru.vood.kotkin.rules.ru.vood.kotkin.rules._03_expression

fun main() {
    println("=== ДЕМО if ВЫРАЖЕНИЙ В KOTLIN ===\n")

    // ----------------------------------------------------------------------
    // 1. БАЗОВОЕ ИСПОЛЬЗОВАНИЕ IF КАК ВЫРАЖЕНИЯ
    // ----------------------------------------------------------------------
    println("1. БАЗОВОЕ ИСПОЛЬЗОВАНИЕ if КАК ВЫРАЖЕНИЯ:")

    // В Kotlin if может быть выражением и возвращать значение
    // Тип результата автоматически выводится как String (общий тип для обеих ветвей)
    val result1: String = if (10 % 2 == 0) {
        // Эта ветвь выполнится, так как условие истинно (10 % 2 = 0)
        "на цело"  // Это значение будет присвоено result1
    } else {
        // Эта ветвь не выполнится в данном случае
        "дробно"   // Если бы условие было ложно, это значение было бы присвоено result1
//        null      // Раскомментирование изменит тип результата на String?
//        throw RuntimeException("Asdsadasd")  // Можно бросать исключения
    }
    println("Результат: $result1")
}