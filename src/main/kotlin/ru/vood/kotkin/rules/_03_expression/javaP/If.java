package ru.vood.kotkin.rules._03_expression.javaP;

public class If {

    public static void main(String[] args) {
        System.out.println("=== ДЕМО if ВЫРАЖЕНИЙ В JAVA ===\n");

        // ----------------------------------------------------------------------
        // 1. БАЗОВОЕ ИСПОЛЬЗОВАНИЕ IF КАК ВЫРАЖЕНИЯ
        // ----------------------------------------------------------------------
        System.out.println("1. БАЗОВОЕ ИСПОЛЬЗОВАНИЕ if КАК ВЫРАЖЕНИЯ:");

        // В Java if является инструкцией, а не выражением
        // Поэтому мы используем временную переменную для эмуляции поведения
        // Огромный минут это мутабельная переменная
        String result1;
        if (10 % 2 == 0) {
            // Эта ветвь выполнится, так как условие истинно (10 % 2 = 0)
            result1 = "на цело";  // Присваиваем значение переменной
        } else {
            // Эта ветвь не выполнится в данном случае
            result1 = "дробно";   // Если бы условие было ложно, присвоили бы это значение
            // result1 = null;    // Раскомментирование возможно, но изменит логику
            // throw new RuntimeException("Asdsadasd");  // Можно бросать исключения
        }


        System.out.println("Результат: " + result1);
    }
}

/*
Kotlin: if является выражением и возвращает значение, которое можно сразу присвоить переменной

Java: if является инструкцией, поэтому требуется предварительное объявление переменной и присваивание в каждой ветке
 */
